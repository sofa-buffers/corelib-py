import sys

from sofab import Encoder

# IEEE-754 limits matching the C ``FLT_MAX`` / ``DBL_MAX``.
FLT_MAX = struct.unpack("<f", b"\xff\xff\x7f\x7f")[0]
DBL_MAX = sys.float_info.max

# 3.14159265f (float literal promoted to double) — as written by test_write_fp64.
FP64_FROM_FLOAT = struct.unpack("<f", struct.pack("<f", 3.14159265))[0]


class ChunkReader:
    """A ``read(n)``-compatible source that hands out at most ``chunk`` bytes per
    call — used to exercise streaming decode at fine granularity (default: 1)."""

    def __init__(self, data: bytes, chunk: int = 1) -> None:
        self._data = bytes(data)
        self._pos = 0
        self._chunk = chunk

    def read(self, n: int) -> bytes:
        if self._pos >= len(self._data):
            return b""
        end = min(self._pos + min(n, self._chunk), len(self._data))
        out = self._data[self._pos : end]
        self._pos = end
        return out


def reader(data: bytes) -> io.BytesIO:
    return io.BytesIO(bytes(data))


def encode(fn) -> bytes:
    """Run ``fn(encoder)`` against a fresh in-memory encoder, return its bytes."""
    enc = Encoder()
    fn(enc)
    return enc.getvalue()


