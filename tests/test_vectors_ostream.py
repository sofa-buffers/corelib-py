@pytest.mark.parametrize("name,fn,expected", SCALAR_CASES + ARRAY_CASES + NESTED,
                         ids=lambda v: v if isinstance(v, str) else "")
def test_vector(name, fn, expected):
    assert encode(fn) == bytes(expected)


def test_full_scale_example():
    enc = Encoder()
    build_full_scale(enc)
    assert enc.getvalue() == FULL_SCALE_EXPECTED


def test_fp32_array_specials_finite_prefix():
    # +0, -0, +inf, -inf (NaN tested separately; multiple bit patterns)
    out = encode(lambda e: e.write_float32_array(0, [0.0, -0.0, math.inf, -math.inf, math.nan]))
    expected_prefix = bytes(
        [0x05, 0x05, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80,
         0x00, 0x00, 0x80, 0x7F, 0x00, 0x00, 0x80, 0xFF]
    )
    assert out[: len(expected_prefix)] == expected_prefix
    assert math.isnan(struct.unpack("<f", out[-4:])[0])
